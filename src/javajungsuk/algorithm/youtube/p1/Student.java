package javajungsuk.algorithm.youtube.p1;

public class Student {
    private String neme;
    private String no;

    public Student(String neme, String no) {
        this.neme = neme;
        this.no = no;
    }


    public String getNeme() {
        return neme;
    }

    public void setNeme(String neme) {
        this.neme = neme;
    }

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }

}
